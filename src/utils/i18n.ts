export function useTranslation<T extends keyof typeof translations>(
  section: T, 
  language: Language = 'de'
): typeof translations[T]['de'] {
  return (translations[section][language] || translations[section].de) as typeof translations[T]['de'];
}

export function getTranslation<T extends keyof typeof translations>(
  section: T, 
  language: Language = 'de'
): typeof translations[T]['de'] {
  return (translations[section][language] || translations[section].de) as typeof translations[T]['de'];
}
